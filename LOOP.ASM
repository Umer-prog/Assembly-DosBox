; program for loop practice
DOSSEG
.MODEL SMALL
.STACK 100H

.DATA
MSG DB 10,13,'ENTER HOW MANY TIMES LOOP SHOULD RUN: $'
NUMBER DB ?
VALUE DB ?
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX

MOV DX,OFFSET MSG
MOV AH,9
INT 21H

MOV AH,1
INT 21H

MOV VALUE,AL
SUB VALUE,48

MOV CL,VALUE

MOV DX,48
L1:

MOV AH,2
INT 21H
INC DX
LOOP L1

MOV AH,4CH
INT 21H

MAIN ENDP
END MAIN
