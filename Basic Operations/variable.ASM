;CODE TO ENTER TO NUMBERS WITH PROMPTS
DOSSEG
.MODEL SMALL
.STACK 100H

.DATA
NUM1 DB 0
NUM2 DB 0
MSG1 DB 10,13, 'ENTER YOUR NUMBER$'
MSG2 DB 10,13, 'SUM IS$'

.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX

MOV DX,OFFSET MSG1;            PRINTING MESSAGE 1
MOV AH,9
INT 21H

MOV AH,1;                      READ 1ST CHAR
INT 21H

MOV NUM1,AL

MOV DX,OFFSET MSG1
MOV AH,9
INT 21H

MOV AH,1;                      READ 2ND CHAR
INT 21H

MOV NUM2,AL

MOV DX,OFFSET MSG2
MOV AH,9
INT 21H

MOV DL,NUM1
ADD DL,NUM2;                   ADDITION
SUB DL,48

MOV AH,2
INT 21H

MOV AH,4CH
INT 21H
MAIN ENDP
END MAIN
